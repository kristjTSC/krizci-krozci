    public static boolean naprej() {
        String vGeslo = vnesenoGeslo;
        boolean aJeOK = false;
        try {

            BufferedReader br = new BufferedReader(new FileReader("registracija.txt"));
            String vrstica;
            while (br.ready()) {
                vrstica = br.readLine();
                String[] tab = vrstica.split("¸");
                String user = tab[0];
                if (ZacetnoOkno.vpisanUser.equalsIgnoreCase(user)) {
                    if (ZacetnoOkno.vpisanoGeslo.equals(vGeslo)) {
                        aJeOK = true;
                    }
                    break;
                }
            }
            br.close();

        } catch (FileNotFoundException ex) {
            Logger.getLogger(ZacetnoOkno.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(ZacetnoOkno.class.getName()).log(Level.SEVERE, null, ex);
        }

        return aJeOK;
    }
