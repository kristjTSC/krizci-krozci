        // TODO add your handling code here:
        vnesenoGeslo = jPasswordField1.getText();
        aJeOK = naprej();
        if (aJeOK) {
            new NadzornaSpremeni().setVisible(true);
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(rootPane, "Napačno geslo!");
            new NadzornaKajZelis().setVisible(true);
            this.dispose();
        }
